#ifndef STD_TYPES_H
#define STD_TYPES_H
//1byte data type
typedef unsigned char u8;
typedef signed char s8;
//2byte data type
typedef short unsigned int u16;
typedef long unsigned int s16;
//4byte data type
typedef unsigned int u32;
typedef signed int s32;
//floating data type
typedef float f32;
typedef double f64;
typedef long double f128;

#endif 